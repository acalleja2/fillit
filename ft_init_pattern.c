#include "fillit.h"

int		ft_init_pattern(char *s)
{
	int i;
	int j;
	int sum;

	i = 0;
	j = 15;
	sum = 0;
	while (s[i] && s[i] != '#')
		i++;
	while (s[i])
	{
		if (s[i] == '.')
		{
			sum = sum | (0 << j);
			j--;
		}
		if (s[i] == '#')
		{
			sum = sum | (1 << j);
			j--;
		}
		i++;
	}
	return (sum);
}
