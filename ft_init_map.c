#include "fillit.h"

unsigned int		*ft_init_map(int a, unsigned int *tab)
{
	int i;

	i = 0;
	while (i < a)
	{
		tab[i] = 2147483647;
		tab[i] = tab[i] >> a;
		i++;
	}
	while (i < 32)
	{
		tab[i] = 2147483647;
		i++;
	}
	return (tab);
}
